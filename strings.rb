# 1. Strings
# A string is a sequence of characters enclosed in quotes. Strings are used to represent text.
#
#   # Example of a string
  greeting = "Hello, world!"
  puts greeting  # Output: Hello, world!
#
# 2. Integers
# An integer is a whole number without a decimal point. Integers are used for counting and arithmetic operations.
#
#   # Example of an integer
  age = 25
  puts age  # Output: 25
#
# 3. Floats
# A float is a number with a decimal point. Floats are used for precise calculations and measurements.
#
  # Example of a float
  pi = 3.14
  puts pi  # Output: 3.14
# 4. Booleans
# A boolean represents a value that can be either true or false. Booleans are used for logical operations and conditions.
#
#   # Example of a boolean
  is_raining = false
  puts is_raining  # Output: false
# 5. Arrays
# An array is an ordered collection of elements. Arrays can contain elements of any type, including other arrays.
#
#   # Example of an array
#   fruits = ["apple", "banana", "cherry"]
# puts fruits.inspect  # Output: ["apple", "banana", "cherry"]
# 6. Hashes
# A hash is a collection of key-value pairs. Hashes are used to store related data and allow quick access to values using keys.
#
#   # Example of a hash
#   person = { name: "Alice", age: 30, city: "London" }
# puts person.inspect  # Output: {:name=>"Alice", :age=>30, :city=>"London"}
# 7. Symbols
# A symbol is a lightweight, immutable identifier often used as keys in hashes. Symbols are more memory-efficient than strings.
#
#   # Example of a symbol
#   status = :active
# puts status  # Output: active
# Summary
# Strings: Represent text.
#   Integers: Whole numbers.
#   Floats: Numbers with decimals.
#   Booleans: true or false.
#   Arrays: Ordered collections.
#   Hashes: Key-value pairs.
#   Symbols: Immutable identifiers.